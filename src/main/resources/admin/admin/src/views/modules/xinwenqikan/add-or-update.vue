// 数字，邮件，手机，url，身份证校验
import { isNumber,isIntNumer,isEmail,isPhone, isMobile,isURL,checkIdCard } from "@/utils/validate";
export default {
	data() {
		let self = this
		var validateIdCard = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!checkIdCard(value)) {
				callback(new Error("请输入正确的身份证号码"));
			} else {
				callback();
			}
		};
		var validateUrl = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isURL(value)) {
				callback(new Error("请输入正确的URL地址"));
			} else {
				callback();
			}
		};
		var validateMobile = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isMobile(value)) {
				callback(new Error("请输入正确的手机号码"));
			} else {
				callback();
			}
		};
		var validatePhone = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isPhone(value)) {
				callback(new Error("请输入正确的电话号码"));
			} else {
				callback();
			}
		};
		var validateEmail = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isEmail(value)) {
				callback(new Error("请输入正确的邮箱地址"));
			} else {
				callback();
			}
		};
		var validateNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isNumber(value)) {
				callback(new Error("请输入数字"));
			} else {
				callback();
			}
		};
		var validateIntNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isIntNumer(value)) {
				callback(new Error("请输入整数"));
			} else {
				callback();
			}
		};
		return {
			id: '',
			type: '',
			
			
			ro:{
				xinwenbiaoti : false,
				xinwenleixing : false,
				xinwenlaiyuan : false,
				xinwenneirong : false,
				fabushijian : false,
				xinwenfengmian : false,
			},
			
			
			ruleForm: {
				xinwenbiaoti: '',
				xinwenleixing: '',
				xinwenlaiyuan: '',
				xinwenneirong: '',
				fabushijian: '',
				xinwenfengmian: '',
			},
		
			
			rules: {
				xinwenbiaoti: [
				],
				xinwenleixing: [
				],
				xinwenlaiyuan: [
					{ validator: validateUrl, trigger: 'blur' },
				],
				xinwenneirong: [
				],
				fabushijian: [
				],
				xinwenfengmian: [
				],
			}
		};
	},
	props: ["parent"],
	computed: {



	},
    components: {
    },
	created() {
		this.ruleForm.fabushijian = this.getCurDate()
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='xinwenbiaoti'){
							this.ruleForm.xinwenbiaoti = obj[o];
							this.ro.xinwenbiaoti = true;
							continue;
						}
						if(o=='xinwenleixing'){
							this.ruleForm.xinwenleixing = obj[o];
							this.ro.xinwenleixing = true;
							continue;
						}
						if(o=='xinwenlaiyuan'){
							this.ruleForm.xinwenlaiyuan = obj[o];
							this.ro.xinwenlaiyuan = true;
							continue;
						}
						if(o=='xinwenneirong'){
							this.ruleForm.xinwenneirong = obj[o];
							this.ro.xinwenneirong = true;
							continue;
						}
						if(o=='fabushijian'){
							this.ruleForm.fabushijian = obj[o];
							this.ro.fabushijian = true;
							continue;
						}
						if(o=='xinwenfengmian'){
							this.ruleForm.xinwenfengmian = obj[o];
							this.ro.xinwenfengmian = true;
							continue;
						}
				}
				






			}
			
			
			// 获取用户信息
